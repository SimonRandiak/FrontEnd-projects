function renderGrid() {
    ctx.moveTo(0,0)
    for (let y = 0; y < pacmanMap.length; y++) {
        for (let x = 0; x < pacmanMap[y].length; x++) {
            if (pacmanMap[y][x] == 1) {
                ctx.fillStyle = "blue"
                ctx.fillRect(x*rectWidth, y*rectHeight, rectWidth, rectHeight)
            }
            else if (pacmanMap[y][x] == 2) {
                ctx.beginPath()
                ctx.strokeStyle = "white"
                ctx.arc(x*rectWidth+ (rectWidth/2), (y*rectHeight) + (rectHeight/2), rectWidth/4, 0, Math.PI * 2)
                ctx.fillStyle = "white"
                ctx.fill()
                ctx.stroke()
            }
        }
    }
}

